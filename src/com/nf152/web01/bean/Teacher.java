package com.nf152.web01.bean;

public class Teacher {
    private int id;
    private String name;
    private String tel;

    public Teacher() {}

    public Teacher(String name, String tel) {
        this.name = name;
        this.tel = tel;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }
}
